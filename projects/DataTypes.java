package projects;

public class DataTypes {

	public static void main(String[] args) {
		
		int first = 15;
		int second = 35;
		int sum = first + second;
		
		float firfl = 15.55f;
		float secfl = 25.55f;
		float sumfl = firfl + secfl;
		
		double firdl = 20.22;
		double secdl = 25.55;
		double sumdl = firdl + secdl;
		
		System.out.println("\n Sum of two integers is: " +sum);
		System.out.println("\n Sum of two floats is: " +sumfl);
		System.out.println("\n sum of two doubles is:" +sumdl);

	}

}
